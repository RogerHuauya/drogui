function ret = SavitzkyGolay(a, n)
    
    coeffs = [];
    if n == 7
        coeffs = [   0.10714,   0.07143,    0.03571, 0,...
                    -0.03571,   -0.07143,   -0.10714];
    elseif n == 5
        coeffs = [0.2, 0.1, 0, -0.1, -0.2];
    elseif n == 10
        coeffs = [  0.05455, 0.04242, 0.0303, 0.01818,...
                    0.00606, -0.00606, -0.01818, -0.0303,... 
                    -0.04242, -0.05455, 0, 0, 0 ];
    elseif n == 13
        coeffs = [  0.03297, 0.02747, 0.02198, 0.01648, ...
                    0.01099, 0.00549, 0, -0.00549, -0.01099, ...
                    -0.01648, -0.02198, -0.02747, -0.03297];  
    end
    
    
    ret = zeros(n-1,1);
    
    arr = zeros(length(a)-n+1, 1);
    
    for i = 1:n
        arr = arr +  coeffs(i)*a((n-i+1):(end-(i-1)));
    end
    
    ret = [ret; arr];
end